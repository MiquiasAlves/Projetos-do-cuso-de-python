# Criando uma lista de frutas
frutas = ["maçã", "banana", "laranja", "abacaxi"]
# Imprimindo a lista de frutas
print("Lista de frutas:")
print(frutas)
# Adicionando uma fruta à lista
frutas.append("uva")
# Imprimindo a lista de frutas atualizada
print("Lista de frutas atualizada:")
print(frutas)
# Removendo uma fruta da lista
frutas.remove("banana")
# Imprimindo a lista de frutas atualizada novamente
print("Lista de frutas atualizada novamente:")
print(frutas)